/*
    String Syntax + Character Escape Sequences
    \ => Escape + Line Continue
    \n => new line
*/

console.log("Hello 'World'"); // "''";
console.log('Hello "World"'); // '""';
console.log('Hello \'World\''); // '\'\'';
console.log("Hello \"World\""); // "\"\"";
console.log("Hello \\Elzero\\"); // "\\";
console.log("Hello \
Elzer \
Web School");
console.log("Hello World!\nI'am From Elzero!");
/*
Hello World! \n
I'am From Elzero!
*/
console.log("Hello\fWorld");
/*
Hello
    World
*/