// User Defined Function:
function sayHello(userName) { // userName => Parameter;
    console.log(`Hi ${userName}`);
} 
// param value => Argument - "Osama" - "Sayed" - "Ali":
sayHello("Osama"); // Hi Osama;
sayHello("Sayed"); // Hi Sayed;
sayHello("Ali"); // Hi Ali;

// Built In Function:
console.log('-'.repeat(50));